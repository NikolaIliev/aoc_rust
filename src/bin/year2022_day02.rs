use aoc_rust::read_input;

fn part_1(input: &str) -> String {
    return "".to_string();
}

fn part_2(input: &str) -> String {
    return "".to_string();
}

fn main() {
    let input = read_input("2022", "02");

    println!("Part 1: {}", part_1(&input));
    println!("Part 2: {}", part_2(&input));
}

#[cfg(test)]
mod tests {
    use super::*;

    #[ignore]
    #[test]
    fn test_part_1() {
        let input = r"";

        assert_eq!(part_1(input), "");
    }

    #[ignore]
    #[test]
    fn test_part_2() {
        let input = r"";

        assert_eq!(part_1(input), "");
    }
}
